  <p>The death of Gopal Krishna Gokhale on 19th February 1915 evoked an outburst of grief and sense of loss throught the country. Leaders like Balagangadhar Tilak and Mahatma Gandhi paid rich tributes to the departed leader. There was a desire every where to make an organised effort to serve the country after the example of gokhale.</p>
  <h4><strong>1915-1941</strong></h4>
  <p>In Bangalore a largely attended public meeting was held on 25th February 1915 at which Sir M.Visveswaraya, then Dewan of Mysore, spoke in appreciation of Gokhale's personality and his work. Inspired by the speeches of national leaders and others, a small group of young men decided to start an association called The Mysore Social Service League. Among those who took active interest in that endeavour may be mentioned D.V.Gundappa, K.S.Krishna Iyer. A.R.Nageshwara Iyer, N.Narasimha Murthy, M.G.Varadachar, C.S.Anantha Padmanabha Iyer, S.Surya Prakash, Mokshagundam Krishna Murthy, Belur Srinivasa Iyengar, B.V.Subba Rao and K.Bheema Rao. It is this small group which decided to invite Mahatma Gandhi to Bangalore in May 1915 and requested him to bless their idea. Gandhiji was touring in South India at that time, and had proposed to visit Madras. A telegram was sent to him and the invitation was accepted</p>
